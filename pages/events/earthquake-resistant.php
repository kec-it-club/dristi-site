          <h2 class="mb-3 mt-5">General Specification:</h2>
          <ul>
            <li style="color:#373c41;">The structure must be able to stand on its own.</li>
            <li style="color:#373c41;">The loads will be added to every floor by the event organizers and the structure must be able to withstand the loads.</li>
            <li style="color:#373c41;">The structure must be able to withstand the vibration from beneath</li>
            <li style="color:#373c41;">The floor area must be less than the base area.</li>
            <li style="color:#373c41;">The superstructure must be attached to the base.</li>
            <li style="color:#373c41;">
            The building must have following dimensions:
                <ul>
                    <li style="color:#373c41;"><b>Building height</b>: 1’6” – 2’</li>
                    <li style="color:#373c41;"><b>Base area</b>: 150- 200 sq. in</li>
                    <li style="color:#373c41;"><b>Number of floors</b>: not less than 5</li>
                </ul>
            </li>
            <li style="color:#373c41;">The teams must submit their models 1- 1.5 hours before the competition for inspection.</li>
            <li style="color:#373c41;">Teams will be given 15 minutes to modify the model which do not qualify the inspection.</li>
            <li style="color:#373c41;">Qualified model will be placed on the table by the team members who may make final adjustments in its position.</li>
            <li style="color:#373c41;">Once the model is in position the loading will be placed on every floor and vibration will be applied from beneath.</li>
            <li style="color:#373c41;">Judging will be based on the last standing structure.</li>
            <li style="color:#373c41;">The building should also be aesthetically appealing. Some marks are allocated for the same.</li>
            <li style="color:#373c41;">
            Materials that can be used are:
                <ul>
                    <li style="color:#373c41;">Balsa wood strips/ MDF timber strips.</li>
                    <li style="color:#373c41;">Papers</li>
                    <li style="color:#373c41;">Base</li>
                    <li style="color:#373c41;">Adhesives like fevicol, movicol and hot glue can be used.</li>
                </ul>
            </li>
            <li style="color:#373c41;">Weight of the building must not exceed 1500gm.
</li>


          </ul>

          <h2 class="mb-3 mt-5">Registration Information:</h2>
